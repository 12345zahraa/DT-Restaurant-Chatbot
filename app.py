import streamlit as st
from typing import Final
from dtx_ollama import chat 

TITLE: Final[str] = "دستیار هوشمند و سیستم ثبت فاکتور رستوران DT 🍔"

st.set_page_config(
    page_icon="🍕",
    page_title=TITLE,
    layout="centered",
)

st.header(
    divider="rainbow",
    body=f"🍕 {TITLE}",
)

# راهنمایی واضح به کاربر برای اینکه سناریوی تک‌مرحله‌ای استاد درست کار کند
st.markdown("""
به سیستم هوشمند رستوران ما خوش آمدید! 
برای دریافت فاکتور و ثبت سفارش، لطفاً **لیست غذاهای درخواستی خود** را در کادر زیر بنویسید.

**مثال:** `من دو تا پیتزا مخلوط و یک نوشابه می‌خوام.`
""")

user_prompt = st.text_input(
    label="سفارش خود را اینجا بنویسید:",
    placeholder="مثلاً: یک چیزبرگر ویژه و یک سیب‌زمینی سرخ‌کرده"
)

user_prompt = user_prompt.strip()

if user_prompt:
    messages: list[dict] = []

    # پرامپت بهینه‌سازی شده برای سناریوی بیزینسی تک‌مرحله‌ای با مدل Gemma2
    system_prompt: str = (
        "تو هسته هوشمند ثبت فاکتور و دستیار رستوران DT هستی. وظیفه تو این است که سفارش کاربر را تحلیل کنی، "
        "قیمت‌ها را حساب کنی و یک فاکتور نهایی و بسیار شیک به زبان فارسی صادر کنی.\n\n"
        
        "--- لیست قیمت‌های منو (به تومان) ---\n"
        "- پیتزا پپرونی مخصوص: ۲۵۰,۰۰۰ تومان\n"
        "- پیتزا مخلوط (میت‌لاور): ۲۸۰,۰۰۰ تومان\n"
        "- پیتزا مرغ و قارچ: ۲۶۰,۰۰۰ تومان\n"
        "- چیزبرگر ویژه: ۲۱۰,۰۰۰ تومان\n"
        "- سیب‌زمینی سرخ‌کرده: ۸۰,۰۰۰ تومان\n"
        "- انواع نوشابه: ۲۰,۰۰۰ تومان\n\n"
        
        "--- قوانین محاسبه و صدور فاکتور ---\n"
        "۱. ابتدا به مشتری سلام کن و از انتخابش تشکر کن.\n"
        "۲. غذاهایی که کاربر در پیامش خواسته را استخراج کن و به صورت یک جدول یا لیست فاکتور با قیمت جلویشان بنویس.\n"
        "۳. جمع کل قیمت‌ها را دقیق حساب کن.\n"
        "۴. اگر جمع کل بالای ۴۰۰,۰۰۰ تومان شد، بنویس: 'هزینه ارسال: رایگان'. در غیر این صورت، مبلغ ۳۰,۰۰۰ تومان هزینه ارسال به فاکتور اضافه کن و جمع نهایی را بگو.\n"
        "۵. در انتها با لحنی بسیار مودبانه بنویس: 'جهت تایید نهایی و ارسال سفارش، لطفا آدرس و شماره تماس خود را ارسال کنید یا با شماره ۰۲۸-۳۳۳۳۳۳۳۳ تماس بگیرید.'\n"
        "۶. اگر کاربر پیامی فرستاد که ربطی به سفارش غذا نداشت، خیلی محترمانه او را راهنمایی کن که این سیستم مخصوص ثبت سفارش رستوران است."
    )
    
    messages.append({"role": "system", "content": system_prompt})
    messages.append({"role": "user", "content": user_prompt})

    with st.spinner("در حال محاسبه و صدور فاکتور... 🧑‍🍳"):
        assistant_answer, _, _ = chat(messages=messages, model_name="gemma2:2b")

    if assistant_answer:
        with st.container(border=True):
            st.write(assistant_answer)